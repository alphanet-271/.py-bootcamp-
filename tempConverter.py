""" A program that performs temperature conversion
Celsius to Fahrenheit = (C x 1.8) + 32
"""
print("This program convert temperature in celsius to fahrenheit")
celsisus = int(input("Enter the value of Celsius to be converted: "))
fahrenheit = (celsisus * 1.8) + 32
print(f'Temp in Fahrenheit: {fahrenheit}F')
